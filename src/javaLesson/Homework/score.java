package javaLesson.Homework;

public class score {
	public static void main(String[] args) {
		int score = 65;
		if (score >= 90) {
			System.out.println("Exelente");
		} else if (score >= 80) {
			System.out.println("Good");
		} else if (score >= 65) {
			System.out.println("Negative");
		} else {
			System.out.println("bad");
		}
	}
}
